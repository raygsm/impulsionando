#!/usr/bin/env node
// Gera src/generated/build-info.ts com commit + timestamp da build.
// Executado automaticamente antes de `vite build` (prebuild / prebuild:dev).
import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = resolve(__dirname, "..", "src", "generated", "build-info.ts");

function safe(cmd) {
  try {
    return execSync(cmd, { stdio: ["ignore", "pipe", "ignore"] }).toString().trim();
  } catch {
    return "";
  }
}

const envSha =
  process.env.LOVABLE_COMMIT_SHA ||
  process.env.GIT_SHA ||
  process.env.CF_PAGES_COMMIT_SHA ||
  process.env.VERCEL_GIT_COMMIT_SHA ||
  "";

const commit = envSha || safe("git rev-parse HEAD") || "unknown";
const branch =
  process.env.LOVABLE_BRANCH ||
  process.env.CF_PAGES_BRANCH ||
  safe("git rev-parse --abbrev-ref HEAD") ||
  "main";
const builtAt = new Date().toISOString();
const mode = process.env.NODE_ENV || "production";

const content = `// AUTO-GENERATED by scripts/generate-build-info.mjs — DO NOT EDIT.
export const BUILD_INFO = {
  commit: ${JSON.stringify(commit)},
  commitShort: ${JSON.stringify(commit.slice(0, 7))},
  branch: ${JSON.stringify(branch)},
  builtAt: ${JSON.stringify(builtAt)},
  mode: ${JSON.stringify(mode)},
  service: "impulsionando",
} as const;

export type BuildInfo = typeof BUILD_INFO;
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, content, "utf8");
console.log(`[build-info] commit=${commit.slice(0, 7)} branch=${branch} builtAt=${builtAt}`);
